 * @param $report interim report generated by processInterimReport()
 */
function printInterimReport( $report ) {
	global $dam;

	echo "<center>
	<table width='80%' class='report'>";
	for($i = 0; $i < count($report); $i++){
      $interim = $report[$i];
      $student = $dam->viewStudent('',$interim['studentid']);
      if($dam->userCanViewInterim('')){
         echo '<tr><td>';
         echo 'Interim: <a href="./viewinterim.php?id='.$interim['interimid'].'">'.$interim['interimid'].'</a><br>';
         echo '<a href="./viewstudent.php?id='.$interim['studentid'].'">'.$student['LAST_NAME'].', '.$student['FIRST_NAME'].' '.$student['MIDDLE_NAME'].'</a><br>';
         echo 'Course: '.$interim['course'].'<br>';
         echo 'Date Processed: '.getShortDate($interim['dateprocessed']);         
         echo '</td></tr>';
      }
   }
	echo "</table></center>";
}




