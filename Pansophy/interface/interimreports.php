function urlGetLine() {
	$getline = "";
	$first = true;
	foreach( $_GET as $attribute => $value ) {
		if( !$first ) $getline .= "&";
		$getline .= $attribute."=".urlencode( $value );
		$first = false;
	}
	if( !$first ) $getline = "?".$getline;
	return $getline;
}

